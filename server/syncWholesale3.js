async function main() {
  await mongoose.connect(MONGO_URI);
  console.log('✅ Подключено\n');

  let updated = 0;
  const notFound = [];

  for (const [name, price] of BY_NAME) {
    const result = await Product.updateMany(
      { name, brand: 'matkasym-home' },
      { $set: { priceWholesale: price } }
    );
    if (result.matchedCount > 0) {
      updated += result.modifiedCount;
      console.log(`✅ [${result.matchedCount}] ${name} → ${price}`);
    } else {
      notFound.push(name);
    }
  }

  console.log(`\n✅ Обновлено: ${updated} документов`);
  if (notFound.length) {
    console.log(`\n⚠️  Не нашёл в БД (${notFound.length}):`);
    notFound.forEach(n => console.log(`   - "${n}"`));
  }

  await mongoose.disconnect();
}

main().catch(e => { console.error(e); process.exit(1); });
